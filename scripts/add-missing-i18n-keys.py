# For brevity, other languages get English fallback for list additions (they'll fall back via i18next)
# We add the critical ones for the major languages

def update_locale_file(lang_code, dashboard_additions, list_additions):
    filepath = os.path.join(LOCALES_DIR, f"{lang_code}.json")
    if not os.path.exists(filepath):
        print(f"  SKIP: {filepath} not found")
        return

    with open(filepath, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Add dashboard keys
    if "dashboard" not in data:
        data["dashboard"] = {}
    
    additions = dashboard_additions.get(lang_code, dashboard_additions.get("en", {}))
    for key, value in additions.items():
        data["dashboard"][key] = value

    # Add list keys
    if "list" not in data:
        data["list"] = {}
    
    list_adds = list_additions.get(lang_code, list_additions.get("en", {}))
    for key, value in list_adds.items():
        if key not in data["list"]:
            data["list"][key] = value

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"  Updated: {lang_code}.json")

# Get all locale files
locale_files = [f.replace(".json", "") for f in os.listdir(LOCALES_DIR) if f.endswith(".json")]
print(f"Found {len(locale_files)} locale files: {locale_files}")

for lang in locale_files:
    update_locale_file(lang, DASHBOARD_ADDITIONS, LIST_ADDITIONS)

print("\nDone! All locale files updated.")
